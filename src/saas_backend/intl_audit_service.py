def extract_weighting_tables(w_df):
    tables = {'spot': None, 'cpt': None, 'repeats': None}
    if w_df is None or w_df.empty: return tables
    
    none_coords = []
    repeat_coords = None
    
    for r in range(len(w_df)):
        for c in range(len(w_df.columns)):
            val = str(w_df.iloc[r, c]).strip().lower()
            if val == '<none>':
                none_coords.append((r, c))
            elif val == 'numberrepeats':
                repeat_coords = (r, c)
                
    if len(none_coords) > 0:
        r, c = none_coords[0]
        t = w_df.iloc[r:r+7, c:].copy()
        t.columns = t.iloc[0].astype(str).str.strip()
        t = t[1:]
        t.iloc[:, 0] = t.iloc[:, 0].astype(str).str.strip().str.lower()
        tables['spot'] = t.set_index(t.columns[0])
        
    if len(none_coords) > 1:
        r, c = none_coords[1]
        t = w_df.iloc[r:r+3, c:].copy()
        t.columns = t.iloc[0].astype(str).str.strip()
        t = t[1:]
        t.iloc[:, 0] = t.iloc[:, 0].astype(str).str.strip().str.lower()
        tables['cpt'] = t.set_index(t.columns[0])
        
    if repeat_coords:
        r, c = repeat_coords
        t = w_df.iloc[r:r+7, c:c+2].copy()
        t.columns = t.iloc[0].astype(str).str.strip()
        t = t[1:]
        t.iloc[:, 0] = t.iloc[:, 0].astype(str).str.replace(r'(?i)\s*broadcast.*', '', regex=True).str.strip()
        tables['repeats'] = t.set_index(t.columns[0])
        
    return tables

def lookup_value(df, ref_df, key_col, val_col):
    if ref_df is None or ref_df.empty: return pd.Series(1.0, index=df.index)
    ref_df = ref_df.copy()
    ref_df.columns = ref_df.columns.astype(str).str.strip().str.lower()
    ref_dict = ref_df.to_dict()
    
    def get_val(row):
        k = str(row.get(key_col)).strip().lower()
        c = str(row.get(val_col)).strip().lower()
        try:
            v = ref_dict.get(c, {}).get(k, 1.0)
            if pd.isna(v) or str(v).strip() == "" or str(v).lower() == "nan": return 1.0
            return safe_pct(v)
        except: return 1.0
    return df.apply(get_val, axis=1)

def process_ref_table(df):
    if df is None or df.empty: return None
    df = df.copy()
    df.columns = df.columns.astype(str).str.strip().str.lower()
    df.iloc[:, 0] = df.iloc[:, 0].astype(str).str.strip().str.lower()
    return df.set_index(df.columns[0])

